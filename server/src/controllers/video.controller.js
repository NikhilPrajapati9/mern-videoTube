export const getAllVideos = asyncHandler(async (req, res) => {
  const { page = 1, limit = 10, query, sortBy, sortType, userId } = req.query;

  const pipeline = [];

  //Search Query (Text search on title/description)
  if (query) {
    pipeline.push({
      $match: {
        $or: [
          {
            title: { $regex: query, $option: "i" },
          },
          {
            description: { $regex: query, $option: "i" },
          },
        ],
      },
    });
  }

  //Filter by userId
  if (userId) {
    if (!isValidObjectId(userId)) {
      throw new ApiError(400, "Invalid User ID");
    }
    pipeline.push({
      $match: {
        owner: new mongoose.Types.ObjectId(userId),
      },
    });
  }

  //Filter only published videos
  pipeline.push({
    $match: { isPublished: true, isDeleted: false },
  });

  //Sorting
  if (sortBy && sortType) {
    pipeline.push({
      $sort: {
        [sortBy]: sortType === "asc" ? 1 : -1,
      },
    });
  } else {
    pipeline.push({
      $sort: { createdAt: -1 },
    });
  }

  //Pagination Options
  const options = {
    page: parseInt(page, 10),
    limit: parseInt(limit, 10),
  };

  //Execute Paginated Aggregation

  const result = await Video.aggregatePaginate(
    Video.aggregate(pipeline),
    options
  );

  if (!result || !result.docs.length === 0) {
    return res.status(200).json(new ApiResponse(200, [], "No videos found"));
  }

  return res
    .status(200)
    .json(new ApiResponse(200, result, "Videos fetched successfully"));
});

export const uploadAVideo = asyncHandler(async (req, res) => {
  const { title, description } = req.body;
  const videoLocalPath = req.files?.videoFile[0]?.path;
  const thumbnailLocalPath = req.files?.thumbnail[0]?.path;

  if (!videoLocalPath) {
    throw new ApiError(400, "video is required");
  }

  const videoFile = await uploadOnCloudinary(videoLocalPath);

  if (!videoFile || !videoFile.playback_url || !videoFile.public_id) {
    throw new ApiError(400, "Failed to upload video");
  }

  let thumbnailFile = null;
  if (thumbnailLocalPath) {
    thumbnailFile = await uploadOnCloudinary(thumbnailLocalPath);
  }

  const newVideo = await Video.create({
    videoFile: {
      url: videoFile.playback_url || videoFile.secure_url || videoFile.url,
      public_id: videoFile.public_id,
    },
    thumbnail: {
      url:
        thumbnailFile?.secure_url ||
        thumbnailFile?.url ||
        videoFile.thumbnail_url?.replace("http://", "https://") ||
        "",
      public_id: thumbnailFile?.public_id || "",
      isAutoGenerated: thumbnailFile?.url ? false : true,
    },
    owner: req.user?._id,
    title: title || `Video_${Date.now()}`,
    description: description ? description.trim() : "",
    duration: videoFile?.duration || 0,
    isPublished: false,
  });

  if (!newVideo) {
    throw new ApiError(400, "Failed to create video");
  }

  return res
    .status(201)
    .json(new ApiResponse(201, newVideo, "Video uploaded successfully."));
});

