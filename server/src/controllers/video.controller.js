export const getAllVideos = asyncHandler(async (req, res) => {
  const {
    page = 1,
    limit = 10,
    query,
    sortBy = "createdAt",
    sortType,
    userId,
  } = req.query;

  const pipeline = [];

  // 1. Initial Filter:
  pipeline.push({
    $match: {
      isPublished: true,
      isDeleted: false,
    },
  });

  // 2. Search Query (Text search)
  if (query) {
    pipeline.push({
      $match: {
        $or: [
          { title: { $regex: query, $options: "i" } },
          { description: { $regex: query, $options: "i" } },
        ],
      },
    });
  }

  // 3. Filter by specific userId
  if (userId) {
    if (!isValidObjectId(userId)) {
      throw new ApiError(400, "Invalid User ID");
    }
    pipeline.push({
      $match: {
        owner: new mongoose.Types.ObjectId(userId),
      },
    });
  }

  // 4. Sorting
  const sortColumn = sortBy || "createdAt";
  const sortDirection = sortType === "asc" ? 1 : -1;

  pipeline.push({
    $sort: {
      [sortColumn]: sortDirection,
    },
  });

  // 5. Pagination Options
  const options = {
    page: parseInt(page, 10),
    limit: parseInt(limit, 10),
  };

  // 6. Execute Paginated Aggregation
  const result = await Video.aggregatePaginate(
    Video.aggregate(pipeline),
    options
  );

  // Result Handling
  if (!result || result.docs.length === 0) {
    return res
      .status(200)
      .json(new ApiResponse(200, { docs: [] }, "No videos found"));
  }

  return res
    .status(200)
    .json(new ApiResponse(200, result, "Videos fetched successfully"));
});

export const getAllDeletedVideos = asyncHandler(async (req, res) => {
  const {
    page = 1,
    limit = 10,
    sortBy = "deletedAt",
    sortType = "desc",
  } = req.query;
  const userId = req.user._id;

  const pipeline = [];

  // 1. Match only DELETED videos of the logged-in user
  pipeline.push({
    $match: {
      owner: new mongoose.Types.ObjectId(userId),
      isDeleted: true,
    },
  });

  // 2. Sorting (Default: Latest deleted first)
  pipeline.push({
    $sort: {
      [sortBy]: sortType === "asc" ? 1 : -1,
    },
  });

  // 3. Lookup: Agar aap video ke owner ki details dikhana chahte ke liye
  pipeline.push({
    $lookup: {
      from: "users",
      localField: "owner",
      foreignField: "_id",
      as: "ownerDetails",
      pipeline: [
        {
          $project: {
            username: 1,
            avatar: 1,
            fullName: 1,
          },
        },
      ],
    },
  });

  pipeline.push({
    $addFields: {
      ownerDetails: { $first: "$ownerDetails" },
    },
  });

  // Pagination Options
  const options = {
    page: parseInt(page, 10),
    limit: parseInt(limit, 10),
  };

  // Execute Paginated Aggregation
  const result = await Video.aggregatePaginate(
    Video.aggregate(pipeline),
    options
  );

  if (!result || result.docs.length === 0) {
    return res
      .status(200)
      .json(new ApiResponse(200, { docs: [] }, "Trash bin is empty"));
  }

  return res
    .status(200)
    .json(
      new ApiResponse(
        200,
        result,
        "Deleted videos fetched successfully from trash"
      )
    );
});

export const uploadAVideo = asyncHandler(async (req, res) => {
  const { title, description } = req.body;
  const videoLocalPath = req.files?.videoFile[0]?.path;
  const thumbnailLocalPath = req.files?.thumbnail[0]?.path;

  if (!videoLocalPath) {
    throw new ApiError(400, "video is required");
  }

  const videoFile = await uploadOnCloudinary(videoLocalPath);

  if (!videoFile || !videoFile.playback_url || !videoFile.public_id) {
    throw new ApiError(400, "Failed to upload video");
  }

  let thumbnailFile = null;
  if (thumbnailLocalPath) {
    thumbnailFile = await uploadOnCloudinary(thumbnailLocalPath);
  }

  const newVideo = await Video.create({
    videoFile: {
      url: videoFile.playback_url || videoFile.secure_url || videoFile.url,
      public_id: videoFile.public_id,
    },
    thumbnail: {
      url:
        thumbnailFile?.secure_url ||
        thumbnailFile?.url ||
        videoFile.thumbnail_url?.replace("http://", "https://") ||
        "",
      public_id: thumbnailFile?.public_id || "",
      isAutoGenerated: thumbnailFile?.url ? false : true,
    },
    owner: req.user?._id,
    title: title || `Video_${Date.now()}`,
    description: description ? description.trim() : "",
    duration: videoFile?.duration || 0,
    isPublished: false,
  });

  if (!newVideo) {
    throw new ApiError(400, "Failed to create video");
  }

  return res
    .status(201)
    .json(new ApiResponse(201, newVideo, "Video uploaded successfully."));
});

export const getVideoById = asyncHandler(async (req, res) => {
  const { videoId } = req.params;
  //TODO: get video by id

  if (!videoId) {
    throw new ApiError(400, "video id is required");
  }

  const video = await Video.aggregate([
    {
      $match: {
        _id: new mongoose.Types.ObjectId(videoId),
      },
    },
    {
      $lookup: {
        from: "likes",
        localField: "_id",
        foreignField: "videoId",
        as: "likes",
      },
    },
    {
      $lookup: {
        from: "users",
        localField: "owner",
        foreignField: "_id",
        as: "owner",
        pipeline: [
          {
            $lookup: {
              from: "subscriptions",
              localField: "_id",
              foreignField: "channel",
              as: "subscribers",
            },
          },
          {
            $addFields: {
              subscribersCount: { $size: "$subscribers" },
              isSubscribed: {
                $cond: {
                  if: { $in: [req.user?._id, "$subscribers.subscriber"] },
                  then: true,
                  else: false,
                },
              },
            },
          },
          {
            $project: {
              fullName: 1,
              username: 1,
              avatar: 1,
              subscribersCount: 1,
              isSubscribed: 1,
            },
          },
        ],
      },
    },
    {
      $addFields: {
        likeCount: { $size: "$likes" },
        owner: { $first: "$owner" },
        isLiked: {
          $cond: {
            if: { $in: [req.user?._id, "$likes.likedBy"] },
            then: true,
            else: false,
          },
        },
      },
    },
    {
      $project: {
        likes: 0,
      },
    },
  ]);

  if (!video.length) {
    throw new ApiError(404, "Video not found");
  }

  return res
    .status(200)
    .json(new ApiResponse(200, video[0], "Video details fetched successfully"));
});

export const updateVideo = asyncHandler(async (req, res) => {
  const { videoId } = req.params;
  const { title, description } = req.body;
  const thumbnailLocalPath = req.file?.path;

  if (!videoId) {
    throw new ApiError(400, "video id is required");
  }

  const video = await Video.findById(videoId);

  if (!video) {
    throw new ApiError(400, "Video not found");
  }

  // Ownership check (Security)
  if (video.owner.toString() !== req.user?._id.toString()) {
    throw new ApiError(403, "Unauthorized request");
  }

  let thumbnailFile = null;

  if (thumbnailLocalPath) {
    thumbnailFile = await uploadOnCloudinary(thumbnailLocalPath);

    if (!thumbnailFile) {
      throw new ApiError(400, "Error while uploading thumbnail");
    }

    // Purani thumbnail delete karo agar nayi aa gayi hai
    // Aur check karo ki purani wali auto-generated toh nahi thi (varna ID nahi hogi)
    if (video.thumbnail?.public_id) {
      try {
        // fileType "image"
        await deleteFromCloudinary(video.thumbnail.public_id, "image");
      } catch (error) {
        console.log("Old thumbnail deletion failed", error);
      }
    }
  }

  const updatedVideo = await Video.findByIdAndUpdate(
    videoId,
    {
      $set: {
        title: title || video.title,
        description: description || video.description,
        thumbnail: {
          url:
            thumbnailFile?.secure_url ||
            thumbnailFile?.url ||
            video.thumbnail.url,
          public_id:
            thumbnailFile?.public_id || video?.thumbnail.public_id || "",
          isAutoGenerated: thumbnailFile
            ? false
            : video.thumbnail.isAutoGenerated,
        },
      },
    },
    {
      new: true,
    }
  );

  return res
    .status(200)
    .json(new ApiResponse(200, updatedVideo, "Video update successfully"));
});

export const deleteVideo = asyncHandler(async (req, res) => {
  const { videoId } = req.params;

  if (!videoId) {
    throw new ApiError(400, "Video Id is required");
  }

  const video = await Video.findById(videoId);

  if (!video) {
    throw new ApiError(404, "Video not found");
  }

  // 2. Authorization check:
  if (video.owner.toString() !== req.user?._id.toString()) {
    throw new ApiError(403, "You do not have permission to delete this video");
  }

  // remove document from database
  await Video.findByIdAndUpdate(videoId, {
    isDeleted: true,
    deletedAt: new Date(),
  });

  return res
    .status(200)
    .json(new ApiResponse(200, null, "Video deleted successfully"));
});

