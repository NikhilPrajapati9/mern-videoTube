export const updateVideo = asyncHandler(async (req, res) => {
  const { videoId } = req.params;
  const { title, description } = req.body;
  const thumbnailLocalPath = req.file?.path;

  if (!videoId) {
    throw new ApiError(400, "video id is required");
  }

  const video = await Video.findById(videoId);

  if (!video) {
    throw new ApiError(400, "Video not found");
  }

  // Ownership check (Security)
  if (video.owner.toString() !== req.user?._id.toString()) {
    throw new ApiError(403, "Unauthorized request");
  }

  let thumbnailFile = null;

  if (thumbnailLocalPath) {
    thumbnailFile = await uploadOnCloudinary(thumbnailLocalPath);

    if (!thumbnailFile) {
      throw new ApiError(400, "Error while uploading thumbnail");
    }

    // Purani thumbnail delete karo agar nayi aa gayi hai
    // Aur check karo ki purani wali auto-generated toh nahi thi (varna ID nahi hogi)
    if (video.thumbnail?.public_id) {
      try {
        // fileType "image"
        await deleteFromCloudinary(video.thumbnail.public_id, "image");
      } catch (error) {
        console.log("Old thumbnail deletion failed", error);
      }
    }
  }

  const updatedVideo = await Video.findByIdAndUpdate(
    videoId,
    {
      $set: {
        title: title || video.title,
        description: description || video.description,
        thumbnail: {
          url:
            thumbnailFile?.secure_url ||
            thumbnailFile?.url ||
            video.thumbnail.url,
          public_id:
            thumbnailFile?.public_id || video?.thumbnail.public_id || "",
          isAutoGenerated: thumbnailFile
            ? false
            : video.thumbnail.isAutoGenerated,
        },
      },
    },
    {
      new: true,
    }
  );

  return res
    .status(200)
    .json(new ApiResponse(200, updatedVideo, "Video update successfully"));
});

